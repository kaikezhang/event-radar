export function normalizeCik(cik: string | null | undefined): string | null {
  if (typeof cik !== 'string') {
    return null;
  }

  const digitsOnly = cik.replace(/\D/g, '');
  if (digitsOnly.length === 0) {
    return null;
  }

  const normalized = digitsOnly.replace(/^0+/, '');
  return normalized.length > 0 ? normalized : '0';
}

export function findTickerByCik(cik: string | null | undefined): string | null {
  const normalized = normalizeCik(cik);
  if (!normalized) {
    return null;
  }

  return CIK_TICKER_MAP[normalized] ?? null;
}
